const mongoose = require('mongoose'); //create const to access package mongoose

const UserSchema = new mongoose.Schema({  //define schema of database and structure
  name: {
    type: String,
    required: true
  },
  email: {
    type: String,
    required: true
  },
  password: {
    type: String,
    required: true
  },
  date: {
    type: Date,
    default: Date.now
  }
});

const User = mongoose.model('User', UserSchema);

module.exports = User;
